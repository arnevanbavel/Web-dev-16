     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function restore($id)
    {
        $specificUser = User::withTrashed()->find($id);
        $specificUser->restore();

        $users = User::where('role', '=', 0)->withTrashed()->get();

